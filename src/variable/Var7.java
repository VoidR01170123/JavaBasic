package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; //Integer
        double b = 10.5; // Real number
        boolean c = true; // Boolean (only true/false can be put)
        char d = 'A'; // Only one letter
        String e = "Hello Java"; // String, special type for String

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
