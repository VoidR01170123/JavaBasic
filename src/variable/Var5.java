package variable;

public class Var5 {
    public static void main(String[] args) {
        //1. Declaring variable and Initializing separately
        int a;
        a = 1;
        System.out.println(a);

        int b = 2; //2. Declaring variable and Initializing tgt
        System.out.println(b);

        int c = 3, d = 4; //3. Declaring numerous variables and Initializing tgt
        System.out.println(c);
        System.out.println(d);
    }

}
